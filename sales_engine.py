def determine_profile(data: dict) -> tuple:
    concerns = set(data.get("concerns", []))
    signals = set(data.get("intent_signals", []))
    budget = data.get("budget", 5000)
    perf = data.get("performance", "中等")

    pts = {k: 0 for k in PROFILE_DESCS}

    if "自主招生/强基" in concerns: pts["名校执念型"] += 30
    if "高考冲刺" in concerns: pts["名校执念型"] += 15
    if budget >= 15000: pts["名校执念型"] += 10
    if perf == "年级前10%": pts["名校执念型"] += 10

    if perf in ("中等偏下", "较差"): pts["焦虑型"] += 25
    if "薄弱科目补差" in concerns: pts["焦虑型"] += 18
    if "提分" in concerns: pts["焦虑型"] += 10
    if data.get("grade") == "高三": pts["焦虑型"] += 15
    if "艺考文化课" in concerns: pts["焦虑型"] += 12

    if "询问价格" in signals: pts["价格敏感型"] += 25
    if budget <= 4000: pts["价格敏感型"] += 20
    if "对比竞品" in signals: pts["价格敏感型"] += 10

    if "询问师资" in signals: pts["理性分析型"] += 20
    if "担心网课效果" in signals: pts["理性分析型"] += 12
    if len(signals) >= 3: pts["理性分析型"] += 15
    if "对比竞品" in signals: pts["理性分析型"] += 10

    if "学习方法" in concerns: pts["素质培养型"] += 28
    if perf in ("年级前10%", "中等偏上"): pts["素质培养型"] += 8

    if "犹豫不决" in signals: pts["佛系型"] += 25
    if len(signals) <= 1 and len(concerns) <= 1: pts["佛系型"] += 15

    profile = max(pts, key=pts.get)
    return profile, PROFILE_DESCS[profile]


