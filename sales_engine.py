# ─── 意向评分 ─────────────────────────────────────────────────────────────────

def calc_intent_score(data: dict) -> int:
    score = 40
    signal_delta = {
        "主动咨询": 15, "要求试听": 20, "询问价格": 8, "询问师资": 7,
        "犹豫不决": -12, "对比竞品": -8, "已在其他机构上课": -15,
        "担心网课效果": -6,
    }
    for s in data.get("intent_signals", []):
        score += signal_delta.get(s, 0)

    perf_delta = {"较差": 12, "中等偏下": 9, "中等": 3, "中等偏上": 0, "年级前10%": 2}
    score += perf_delta.get(data.get("performance", ""), 0)

    concerns = set(data.get("concerns", []))
    if "高考冲刺" in concerns: score += 12
    if "艺考文化课" in concerns: score += 10
    if "薄弱科目补差" in concerns: score += 7
    if "提分" in concerns: score += 5

    b = data.get("budget", 5000)
    if b >= 15000: score += 8
    elif b >= 8000: score += 4
    elif b <= 2000: score -= 8

    grade = data.get("grade", "")
    if grade == "高三": score += 14
    elif grade == "高二": score += 7
    elif grade == "高一": score += 3

    return max(5, min(97, score))


